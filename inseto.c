#include <stdio.h>
 
int main() {
    
    int Quantidade, Forca, i;
    i == 0;
    scanf("%d",&Quantidade);
    while (Quantidade > i)
    {
        scanf("%d",&Forca);
        if (Forca > 8000)
        {
            printf("Mais de 8000!\n");
        }
        else{
            printf("Inseto!\n");
        }
        i++;
        
    }
    
 
    return 0;
}